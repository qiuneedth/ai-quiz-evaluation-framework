# Generic cleanup
# ============================================================

def _generic_profile_cleanup(profile: dict) -> dict:
    """
    Apply generic cleanup when there is no dataset-specific rule.

    This handles common cases:
    - has_options -> multiple_choice
    - has_context -> context_grounded_qa
    - no context but reference answer -> factoid_qa
    """

    qa = profile["question_answer_profile"]
    structure = profile["dataset_structure"]
    evaluation = profile["evaluation_profile"]

    has_context = structure.get("has_context", False)
    has_options = structure.get("has_options", False)
    has_reference_answer = structure.get("has_reference_answer", False)
    has_generated_response = structure.get("has_generated_response", False)

    if has_generated_response:
        qa["question_type"] = "rag_response_evaluation"
        qa["answer_type"] = "generated_response"
        qa["reasoning_type"] = "contextual_reasoning"
        qa["context_dependency"] = "retrieved_context_required"

        evaluation["evaluation_candidates"] = [
            "relevance_evaluator",
            "groundedness_evaluator",
            "completeness_evaluator",
        ]

        evaluation["possible_metrics"] = [
            "relevance",
            "groundedness",
            "completeness",
            "hallucination_rate",
        ]

    elif has_options:
        qa["question_type"] = "multiple_choice"
        qa["answer_type"] = "multiple_choice_option"
        qa["reasoning_type"] = "logical_reasoning"
        qa["context_dependency"] = "none"

        evaluation["evaluation_candidates"] = [
            "rule_based_evaluator",
        ]

        evaluation["possible_metrics"] = [
            "exact_match",
            "correctness",
        ]

    elif has_context:
        qa["question_type"] = "context_grounded_qa"
        qa["answer_type"] = "short_text"
        qa["reasoning_type"] = "contextual_reasoning"
        qa["context_dependency"] = "provided_context_required"

        evaluation["evaluation_candidates"] = [
            "llm_context_evaluator",
            "groundedness_evaluator",
        ]

        evaluation["possible_metrics"] = [
            "correctness",
            "context_usage",
            "groundedness",
        ]

    elif has_reference_answer:
        qa["question_type"] = "factoid_qa"
        qa["answer_type"] = "free_form_text"
        qa["reasoning_type"] = "factual_recall"
        qa["context_dependency"] = "none"

        evaluation["evaluation_candidates"] = [
            "keyword_evaluator",
            "llm_evaluator",
        ]

        evaluation["possible_metrics"] = [
            "keyword_coverage",
            "correctness",
        ]

    return profile



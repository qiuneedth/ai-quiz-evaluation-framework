def _postprocess_hotpotqa(profile: dict, samples: list[dict]) -> dict:
    """
    HotpotQA should be treated as multi-hop context-grounded QA.

    It should not be treated as simple short-answer QA.
    """

    qa = profile["question_answer_profile"]
    structure = profile["dataset_structure"]
    semantic = profile["semantic_profile"]
    evaluation = profile["evaluation_profile"]

    structure["dataset_category"] = "multi_hop_context_qa"
    structure["has_context"] = True
    structure["has_reference_answer"] = True
    structure["has_options"] = False

    qa["question_type"] = "multi_hop_reasoning"
    qa["answer_type"] = "short_text"
    qa["reasoning_type"] = "multi_hop_reasoning"
    qa["difficulty_level"] = _infer_metadata_value(
        samples=samples,
        metadata_key="difficulty_level",
        fallback="medium",
    )
    qa["context_dependency"] = "provided_context_required"
    qa["expected_answer_format"] = "short_text"

    semantic["domain"] = "open_domain"
    semantic["topic"] = "multi_hop_wikipedia_qa"

    evaluation["evaluation_candidates"] = [
        "llm_context_evaluator",
        "groundedness_evaluator",
        "completeness_evaluator",
        "reasoning_quality_evaluator",
    ]

    evaluation["possible_metrics"] = [
        "correctness",
        "context_usage",
        "groundedness",
        "completeness",
        "reasoning_quality",
        "hallucination_rate",
    ]

    profile["notes"] = (
        "Postprocessed as multi-hop context-grounded QA. "
        "Recommended evaluation focuses on context correctness, groundedness, "
        "completeness, and reasoning quality."
    )

    return profile



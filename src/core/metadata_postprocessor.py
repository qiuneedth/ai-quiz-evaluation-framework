# src/core/metadata_postprocessor.py

"""
Metadata Postprocessor.

This module has two responsibilities.

1. Legacy metadata post-processing
   The old pipeline may produce flat metadata such as:
   - recommended_evaluation_methods
   - recommended_metrics
   - has_context
   - has_reference_answer

   The function postprocess_metadata() keeps this old behavior.

2. Dataset Profile post-processing
   The new framework uses a structured Dataset Profile:
   - dataset_identity
   - dataset_structure
   - question_answer_profile
   - semantic_profile
   - evaluation_profile
   - sample_profile

   The function postprocess_profile() improves this structured profile.

Why this file is important:
The basic Dataset Analyzer can detect simple structure, but it may produce
weak labels such as:
- short_answer
- short_question
- open_ended
- unknown

For the thesis, these labels are too weak.

This postprocessor converts weak metadata into stronger evaluation-oriented
types such as:
- multiple_choice
- boolean_reasoning
- factoid_qa
- claim_verification
- context_grounded_qa
- multi_hop_reasoning
- rag_response_evaluation

Important:
This module is rule-based.
It does not use AI.
It makes the profile more explainable and reproducible.
"""


from collections import Counter



def _normalize_profile_taxonomy(profile: dict) -> dict:
    """
    Normalize final profile labels to formal taxonomy.

    If an unsupported label appears, map it to a formal one or unknown.
    """

    qa = profile.get("question_answer_profile", {})

    question_type = qa.get("question_type", "unknown")
    answer_type = qa.get("answer_type", "unknown")
    reasoning_type = qa.get("reasoning_type", "unknown")
    context_dependency = qa.get("context_dependency", "unknown")

    if question_type not in FORMAL_QUESTION_TYPES:
        qa["question_type"] = _map_weak_question_type(question_type)

    if answer_type not in FORMAL_ANSWER_TYPES:
        qa["answer_type"] = _map_weak_answer_type(answer_type)

    if reasoning_type not in FORMAL_REASONING_TYPES:
        qa["reasoning_type"] = "unknown"

    if context_dependency not in FORMAL_CONTEXT_DEPENDENCY:
        qa["context_dependency"] = "unknown"

    return profile


def _map_weak_question_type(question_type: str) -> str:
    """
    Convert weak question_type labels into formal labels.
    """

    mapping = {
        "short_question": "factoid_qa",
        "short_answer": "factoid_qa",
        "simple_qa": "factoid_qa",
        "open_ended": "open_ended_reasoning",
        "true_false": "boolean_reasoning",
        "yes_no": "boolean_reasoning",
        "context_based_qa": "context_grounded_qa",
        "mcq": "multiple_choice",
    }

    return mapping.get(question_type, "unknown")


def _map_weak_answer_type(answer_type: str) -> str:
    """
    Convert weak answer_type labels into formal labels.
    """

    mapping = {
        "short_answer": "short_text",
        "free_text": "free_form_text",
        "multiple_choice": "multiple_choice_option",
        "option": "multiple_choice_option",
        "yes_no": "boolean",
        "true_false": "boolean",
    }

    return mapping.get(answer_type, "unknown")


def _ensure_profile_sections(profile: dict) -> dict:
    """
    Ensure all major profile sections exist.

    This prevents KeyError in dataset-specific postprocessors.
    """

    profile.setdefault("dataset_identity", {})
    profile.setdefault("dataset_structure", {})
    profile.setdefault("question_answer_profile", {})
    profile.setdefault("semantic_profile", {})
    profile.setdefault("evaluation_profile", {})
    profile.setdefault("sample_profile", {})

    return profile


def _ensure_profile_defaults(profile: dict) -> dict:
    """
    Ensure important fields exist after post-processing.
    """

    structure = profile["dataset_structure"]
    qa = profile["question_answer_profile"]
    semantic = profile["semantic_profile"]
    evaluation = profile["evaluation_profile"]

    structure.setdefault("has_context", False)
    structure.setdefault("has_options", False)
    structure.setdefault("has_reference_answer", False)
    structure.setdefault("has_generated_response", False)

    qa.setdefault("question_type", "unknown")
    qa.setdefault("answer_type", "unknown")
    qa.setdefault("reasoning_type", "unknown")
    qa.setdefault("difficulty_level", "unknown")
    qa.setdefault("context_dependency", "unknown")
    qa.setdefault("expected_answer_format", "unknown")


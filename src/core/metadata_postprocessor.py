# Legacy function
# ============================================================

def postprocess_metadata(metadata: dict) -> dict:
    """
    Clean and validate old flat dataset metadata.

    This function is kept for backward compatibility with the old pipeline.

    Input:
        Flat metadata generated by an earlier Dataset Analyzer.

    Output:
        Filtered metadata with:
        - filtered_evaluation_methods
        - filtered_metrics
        - postprocessing_notes
    """

    methods = metadata.get("recommended_evaluation_methods", [])
    metrics = metadata.get("recommended_metrics", [])

    has_context = metadata.get("has_context", False)
    has_reference_answer = metadata.get("has_reference_answer", False)
    question_type = metadata.get("question_type", "unknown")

    filtered_methods = list(methods)
    filtered_metrics = list(metrics)

    # Rule 1:
    # If there is no context, RAG evaluation is not appropriate.
    if not has_context:
        filtered_methods = [
            method for method in filtered_methods
            if method not in RAG_METHODS
        ]

        filtered_metrics = [
            metric for metric in filtered_metrics
            if metric not in GROUNDING_METRICS
        ]

    # Rule 2:
    # If there is no reference answer, exact match is not appropriate.
    if not has_reference_answer:
        filtered_methods = [
            method for method in filtered_methods
            if method not in REFERENCE_REQUIRED_METHODS
        ]

    # Rule 3:
    # True/false datasets should prioritize binary rule-based evaluation.
    if question_type in ["true_false", "boolean_reasoning"]:
        if "rule_based_exact_match" not in filtered_methods:
            filtered_methods.insert(0, "rule_based_exact_match")

        if "correctness" not in filtered_metrics:
            filtered_metrics.insert(0, "correctness")

    # Rule 4:
    # If no method remains, fallback to LLM-as-a-Judge.
    if not filtered_methods:
        filtered_methods = ["llm_as_judge"]

    metadata["filtered_evaluation_methods"] = filtered_methods
    metadata["filtered_metrics"] = filtered_metrics

    metadata["postprocessing_notes"] = (
        "Rules applied: remove RAG methods if no context; "
        "remove exact match if no reference answer; "
        "prioritize rule-based evaluation for boolean questions."
    )

    return metadata


# ============================================================
# New function for structured Dataset Profile
# ============================================================

def postprocess_profile(
    profile: dict,
    samples: list[dict],
    dataset_key: str,
) -> dict:
    """
    Improve a structured Dataset Profile.

    This is the main function used by the new framework.

    Input:
        profile:
            Initial profile from dataset_analyzer.py.

        samples:
            Adapted samples from hf_adapters.py.

        dataset_key:
            Dataset key used in your Hugging Face registry.

    Output:
        Improved profile with stronger taxonomy and evaluation metadata.

    Main improvements:
        - Fix weak question types.
        - Fix StrategyQA yes/no as boolean_reasoning.
        - Fix HotpotQA as multi_hop_reasoning.
        - Fix ARC as multiple_choice.
        - Fix RAGBench as rag_response_evaluation.
        - Fill evaluation_candidates.
        - Fill possible_metrics.
    """

    profile = _ensure_profile_sections(profile)

    # Dataset-specific rules should run first.
    # These rules are stronger than generic structural detection.
    if dataset_key == "hotpotqa":
        profile = _postprocess_hotpotqa(profile, samples)

    elif dataset_key == "ragbench_covidqa":
        profile = _postprocess_ragbench_covidqa(profile, samples)

    elif dataset_key == "natural_questions_pair":
        profile = _postprocess_natural_questions_pair(profile, samples)

    elif dataset_key == "ai2_arc_challenge":
        profile = _postprocess_ai2_arc_challenge(profile, samples)

    elif dataset_key == "strategyqa":
        profile = _postprocess_strategyqa(profile, samples)

    elif dataset_key == "fever":
        profile = _postprocess_fever(profile, samples)

    else:
        profile = _generic_profile_cleanup(profile)

    # Normalize again after dataset-specific rules.
    profile = _normalize_profile_taxonomy(profile)

    # Make sure downstream modules always have required fields.
    profile = _ensure_profile_defaults(profile)

    return profile



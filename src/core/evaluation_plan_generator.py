# src/core/evaluation_plan_generator.py

"""
Evaluation Plan Generator.

This module converts selected evaluators into a full Evaluation Plan.

Important:
selected_evaluators is only a list.
EvaluationPlan is an executable workflow.

The Evaluation Plan defines:
- evaluator execution order
- input fields
- prompt templates
- metrics
- evaluator rules
- score rubrics
- aggregation rule
- scoring strategy
- report requirements

This version reads manually designed evaluator rules from:
src/core/evaluator_rulebook.py

Therefore, the generated plan can explicitly show:
- what each evaluator does
- what inputs it needs
- what metrics it produces
- how it should score answers
"""


from copy import deepcopy

from src.core.evaluator_rulebook import get_evaluator_rule, resolve_rule_name


class EvaluationPlanGenerator:
    """
    Generate a structured Evaluation Plan from:
    - Dataset Profile
    - selected evaluator names

    The generated Evaluation Plan is used by EvaluatorExecutor later.
    """

    def generate_plan(self, profile: dict, selected_evaluators: list[str]) -> dict:
        """
        Generate a complete evaluation plan.

        Input:
            profile:
                Dataset Profile generated by Dataset Analyzer.

            selected_evaluators:
                Evaluator names selected by Evaluation Manager.

        Output:
            A dictionary representing the Evaluation Plan.
        """

        dataset_name = profile["dataset_identity"]["dataset_name"]
        qa_profile = profile.get("question_answer_profile", {})

        question_type = qa_profile.get("question_type", "unknown")
        reasoning_type = qa_profile.get("reasoning_type", "unknown")
        difficulty_level = qa_profile.get("difficulty_level", "unknown")
        context_dependency = qa_profile.get("context_dependency", "unknown")

        evaluation_sequence = []

        # ------------------------------------------------------------
        # Convert each selected evaluator into a detailed evaluation step.
        # ------------------------------------------------------------
        for index, evaluator_name in enumerate(selected_evaluators, start=1):
            step = self._build_step(
                step_order=index,
                evaluator_name=evaluator_name,
                profile=profile,
            )
            evaluation_sequence.append(step)

        # ------------------------------------------------------------
        # Build metric weights according to all metrics in the plan.
        # ------------------------------------------------------------
        scoring_strategy = self._build_scoring_strategy(
            profile=profile,
            evaluation_sequence=evaluation_sequence,
        )

        # ------------------------------------------------------------
        # Build final plan object.
        # ------------------------------------------------------------
        plan = {
            "plan_identity": {
                "plan_id": f"plan_{dataset_name.lower()}_{question_type}",
                "plan_name": f"{question_type}_{reasoning_type}_evaluation_plan",
                "dataset_name": dataset_name,
            },

            "plan_context": {
                "question_type": question_type,
                "reasoning_type": reasoning_type,
                "difficulty_level": difficulty_level,
                "context_dependency": context_dependency,
            },

            # This is the most important part of the plan.
            # Each step now contains evaluator rules and score rubric.
            "evaluation_sequence": evaluation_sequence,

            "aggregation": {
                "aggregation_rule": self._select_aggregation_rule(profile),
                "scoring_strategy": scoring_strategy,
                "hard_constraints": self._build_hard_constraints(
                    profile=profile,
                    evaluation_sequence=evaluation_sequence,
                ),
            },

            "reporting": {
                "include_per_question_result": True,
                "include_metric_summary": True,
                "include_failed_cases": True,
                "include_explanations": True,
                "include_rule_trace": True,
            },
        }

        return plan

    def _build_step(self, step_order: int, evaluator_name: str, profile: dict) -> dict:
        """
        Build one evaluation step.

        This method reads the evaluator rule from evaluator_rulebook.py.

        Each step tells the executor:
        - which evaluator to call
        - which input fields are required
        - which prompt should be loaded
        - which metrics should be produced
        - which rules should be applied
        - which scoring rubric should be used
        """

        # ------------------------------------------------------------
        # Step 1:
        # Read manually designed rule from the rulebook.
        # ------------------------------------------------------------
        evaluator_rule = get_evaluator_rule(evaluator_name)

        # ------------------------------------------------------------
        # Step 2:
        # Resolve old evaluator names to clearer rule names.
        #
        # Example:
        # llm_context_evaluator
        # -> context_correctness_evaluator
        # ------------------------------------------------------------
        rule_name = resolve_rule_name(evaluator_name)

        # ------------------------------------------------------------
        # Step 3:
        # Copy values from rulebook.
        #
        # deepcopy prevents accidental mutation of the global rulebook.
        # ------------------------------------------------------------
        input_fields = deepcopy(evaluator_rule.get("input_fields", []))
        metrics = deepcopy(evaluator_rule.get("metrics", []))
        rules = deepcopy(evaluator_rule.get("rules", {}))
        score_rubric = deepcopy(evaluator_rule.get("score_rubric", {}))
        hard_constraints = deepcopy(evaluator_rule.get("hard_constraints", {}))
        limitations = deepcopy(evaluator_rule.get("limitations", []))

        prompt_template = evaluator_rule.get("prompt_template")
        evaluator_type = evaluator_rule.get("evaluator_type", "unknown")
        purpose = evaluator_rule.get("purpose", "")

        # ------------------------------------------------------------
        # Step 4:
        # Build the evaluation step object.
        # ------------------------------------------------------------
        return {
            "step": step_order,

            # Keep the original evaluator name because your executor/registry
            # may still use this name.
            "evaluator": evaluator_name,

            # Store the clearer rule name for explanation and reporting.
            "rule_name": rule_name,

            "evaluator_type": evaluator_type,
            "purpose": purpose,

            "input_fields": input_fields,
            "prompt_template": prompt_template,
            "metrics": metrics,

            # This is the key improvement:
            # rules are no longer empty.
            "rules": rules,

            # This makes scoring criteria visible in the plan.
            "score_rubric": score_rubric,

            # Optional constraints and limitations.
            "hard_constraints": hard_constraints,
            "limitations": limitations,
        }

    def _build_scoring_strategy(
        self,
        profile: dict,
        evaluation_sequence: list[dict],
    ) -> dict:
        """
        Build metric weights for final score aggregation.

        This method reads all metrics from the generated evaluation steps.

        The weights below are manually designed initial defaults.
        They can be adjusted later as part of your thesis experiments.
        """

        qa_profile = profile.get("question_answer_profile", {})
        context_dependency = qa_profile.get("context_dependency", "unknown")
        reasoning_type = qa_profile.get("reasoning_type", "unknown")
        question_type = qa_profile.get("question_type", "unknown")

        all_metrics = self._collect_metrics(evaluation_sequence)

        weights = {}

        # ------------------------------------------------------------
        # Context-based QA:
        # Correctness and groundedness should be more important.
        # ------------------------------------------------------------
        if context_dependency in [
            "provided_context_required",
            "retrieved_context_required",
        ]:
            default_weights = {
                "correctness": 0.30,
                "context_usage": 0.15,
                "groundedness": 0.25,
                "hallucination_rate": 0.10,
                "faithfulness": 0.20,
                "completeness": 0.15,
                "relevance": 0.10,
                "reasoning_quality": 0.15,
            }

        # ------------------------------------------------------------
        # Multiple-choice or objective QA:
        # Exact match is the primary metric.
        # ------------------------------------------------------------
        elif question_type == "multiple_choice":
            default_weights = {
                "exact_match": 0.80,
                "correctness": 0.20,
            }

        # ------------------------------------------------------------
        # Reasoning tasks:
        # Correctness and reasoning quality are both important.
        # ------------------------------------------------------------
        elif reasoning_type in [
            "multi_hop_reasoning",
            "commonsense_reasoning",
            "logical_reasoning",
            "open_ended_reasoning",
        ]:
            default_weights = {
                "correctness": 0.40,
                "reasoning_quality": 0.30,
                "completeness": 0.20,
                "relevance": 0.10,
            }

        # ------------------------------------------------------------
        # General fallback:
        # Mainly correctness, with optional relevance/completeness.
        # ------------------------------------------------------------
        else:
            default_weights = {
                "correctness": 0.50,
                "keyword_coverage": 0.20,
                "relevance": 0.15,
                "completeness": 0.15,
            }

        # ------------------------------------------------------------
        # Only keep weights for metrics that actually appear in the plan.
        # ------------------------------------------------------------
        for metric in all_metrics:
            if metric in default_weights:
                weights[metric] = default_weights[metric]
            else:
                # If a metric is not listed above, give it a small default weight.
                weights[metric] = 0.10

        return self._normalize_weights(weights)

    def _collect_metrics(self, evaluation_sequence: list[dict]) -> list[str]:
        """
        Collect all unique metrics from evaluation steps.

        The order is preserved.
        """

        metrics = []

        for step in evaluation_sequence:
            for metric in step.get("metrics", []):
                if metric not in metrics:
                    metrics.append(metric)

        return metrics

    def _normalize_weights(self, weights: dict) -> dict:
        """
        Normalize metric weights so their sum equals 1.

        This makes the scoring strategy stable even when different
        evaluator combinations produce different metric sets.
        """

        total = sum(weights.values())

        if total == 0:
            return {}

        normalized = {}

        for metric, weight in weights.items():
            normalized[metric] = round(weight / total, 4)

        return normalized

    def _select_aggregation_rule(self, profile: dict) -> str:
        """
        Select an aggregation rule based on profile.

        For context-based datasets, groundedness should act as a constraint.
        For other datasets, weighted average is enough as a starting point.
        """

        qa_profile = profile.get("question_answer_profile", {})
        context_dependency = qa_profile.get("context_dependency", "unknown")

        if context_dependency in [
            "provided_context_required",
            "retrieved_context_required",
        ]:
            return "weighted_average_with_groundedness_constraint"

        return "weighted_average"

    def _build_hard_constraints(
        self,
        profile: dict,
        evaluation_sequence: list[dict],
    ) -> dict:
        """
        Build hard constraints for aggregation.

        Hard constraints are rules that affect final scoring beyond
        normal weighted average.

        Example:
        If groundedness is very low, the final score should be capped,
        even if correctness seems high.
        """

        qa_profile = profile.get("question_answer_profile", {})
        context_dependency = qa_profile.get("context_dependency", "unknown")

        all_metrics = self._collect_metrics(evaluation_sequence)

        constraints = {}

        # ------------------------------------------------------------
        # For context-based evaluation, groundedness is important.
        # If answer is not grounded, final score should not be high.
        # ------------------------------------------------------------
        if (
            context_dependency in [
                "provided_context_required",
                "retrieved_context_required",
            ]
            and "groundedness" in all_metrics
        ):
            constraints["groundedness_constraint"] = {
                "enabled": True,
                "if_groundedness_below": 0.3,
                "cap_final_score_at": 0.5,
                "reason": "For context-dependent QA, an unsupported answer should not receive a high final score.",
            }

        # ------------------------------------------------------------
        # Hallucination can also cap the final score.
        # ------------------------------------------------------------
        if "hallucination_rate" in all_metrics:
            constraints["hallucination_constraint"] = {
                "enabled": True,
                "if_hallucination_rate_above": 0.7,
                "cap_final_score_at": 0.4,
                "reason": "Highly hallucinated answers should receive a low final score.",
            }

        return constraints
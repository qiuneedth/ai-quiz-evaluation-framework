"""
Planner Rulebook.

This file defines manually designed rules for how questions are provided
to the user during an evaluation session.

This rulebook is different from evaluator_rulebook.py.

Difference:

1. evaluator_rulebook.py
   - Defines how to evaluate user answers.
   - Example: correctness, relevance, completeness, groundedness.

2. planner_rulebook.py
   - Defines how to deliver questions to the user.
   - Example: one-by-one ordered, random batch, full evaluation,
     partial evaluation, unseen questions only.

This file corresponds to the supervisor's diagram:

Generate a plan:
- collection plan
- progress plan
- question-selection plan

Important:
These planner rules are NOT generated by AI.
They are manually designed framework rules.
"""



def get_planner_rule(category: str, strategy_name: str) -> dict:
    """
    Return the rule description for one planner strategy.

    Parameters:
        category:
            One of:
            - collection_strategy
            - progress_strategy
            - question_selection_strategy

        strategy_name:
            The selected strategy name.

    Returns:
        A dictionary containing description, use_case, and behavior.
    """

    if category not in PLANNER_RULEBOOK:
        raise ValueError(
            f"Unknown planner rule category: {category}. "
            f"Available categories: {list(PLANNER_RULEBOOK.keys())}"
        )

    if strategy_name not in PLANNER_RULEBOOK[category]:
        raise ValueError(
            f"Unknown strategy '{strategy_name}' for category '{category}'. "
            f"Available strategies: {list(PLANNER_RULEBOOK[category].keys())}"
        )

    return PLANNER_RULEBOOK[category][strategy_name]


def explain_planner_config(planner_config: dict) -> dict:
    """
    Convert a planner config into human-readable rule explanations.

    This is useful for:
    - demo output
    - reports
    - user activity logs
    - explaining the system to the supervisor

    Input example:
        {
            "collection_strategy": "one_by_one_ordered",
            "progress_strategy": "partial_eval",
            "question_selection_strategy": "unseen_only"
        }

    Output:
        {
            "collection_plan": {...},
            "progress_plan": {...},
            "question_selection_plan": {...}
        }
    """

    collection_strategy = planner_config.get("collection_strategy")
    progress_strategy = planner_config.get("progress_strategy")
    question_selection_strategy = planner_config.get("question_selection_strategy")

    return {
        "collection_plan": {
            "strategy": collection_strategy,
            "rule": get_planner_rule(
                category="collection_strategy",
                strategy_name=collection_strategy,
            ),
        },

        "progress_plan": {
            "strategy": progress_strategy,
            "rule": get_planner_rule(
                category="progress_strategy",
                strategy_name=progress_strategy,
            ),
        },

        "question_selection_plan": {
            "strategy": question_selection_strategy,
            "rule": get_planner_rule(
                category="question_selection_strategy",
                strategy_name=question_selection_strategy,
            ),
        },
    }
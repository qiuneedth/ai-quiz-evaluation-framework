# src/core/evaluation_router.py

"""
Evaluation Router.

This file converts dataset metadata into a concrete evaluation plan.

Why this file exists:
The Dataset Analyzer tells us what the dataset looks like.
For example:
- Does it have context?
- Is it multiple choice?
- Is it true/false?
- Does it have a reference answer?
- What methods are possible?

The Evaluation Router decides what we should actually use.

Pipeline position:

dataset
→ dataset analyzer
→ metadata
→ evaluation router
→ evaluation plan
→ evaluator

This corresponds to the framework layer:

Question analysis / evaluation method selection.
"""


def route_evaluation(metadata: dict) -> dict:
    """
    Create an evaluation plan from dataset metadata.

    Args:
        metadata:
            A metadata dictionary generated by dataset_analyzer.py
            and cleaned by metadata_postprocessor.py.

    Returns:
        evaluation_plan:
            A structured dictionary describing:
            - selected evaluation methods
            - selected metrics
            - scoring mode
            - explanation of routing decision
    """

    dataset_name = metadata.get("dataset_name", "unknown_dataset")

    # Basic dataset features
    has_context = metadata.get("has_context", False)
    has_options = metadata.get("has_options", False)
    has_reference_answer = metadata.get("has_reference_answer", False)
    question_type = metadata.get("question_type", "unknown")
    answer_type = metadata.get("answer_type", "unknown")
    reasoning_required = metadata.get("reasoning_required", False)

    # Methods and metrics after post-processing
    available_methods = metadata.get(
        "filtered_evaluation_methods",
        metadata.get("recommended_evaluation_methods", [])
    )

    available_metrics = metadata.get(
        "filtered_metrics",
        metadata.get("recommended_metrics", [])
    )

    selected_methods = []
    selected_metrics = []
    scoring_mode = "unknown"
    routing_reason = []

    # =====================================================
    # Case 1: Multiple choice questions
    # =====================================================
    if question_type == "mcq" or has_options:
        selected_methods = ["rule_based_exact_match"]
        selected_metrics = ["correctness"]
        scoring_mode = "binary"

        routing_reason.append(
            "The dataset contains multiple-choice questions, so rule-based exact match is the primary method."
        )

    # =====================================================
    # Case 2: True/false or boolean questions
    # =====================================================
    elif question_type == "true_false" or answer_type == "boolean":
        selected_methods = ["rule_based_exact_match"]

        # LLM judge may still be useful if reasoning is required
        if reasoning_required:
            selected_methods.append("llm_as_judge")

        selected_metrics = ["correctness"]
        scoring_mode = "binary"

        routing_reason.append(
            "The dataset contains boolean or true/false answers, so binary correctness is the main metric."
        )

        if reasoning_required:
            routing_reason.append(
                "Reasoning is required, so LLM-as-a-Judge can be used as an additional method."
            )

    # =====================================================
    # Case 3: Context-based QA
    # =====================================================
    elif has_context:
        selected_methods = []

        # Prefer RAG/rubric methods if available
        if "rag_rubric_judge" in available_methods:
            selected_methods.append("rag_rubric_judge")
        elif "rag_based_evaluation" in available_methods:
            selected_methods.append("rag_based_evaluation")
        else:
            selected_methods.append("rag_based_evaluation")

        # LLM-as-a-Judge can be used as comparison baseline
        if "llm_as_judge" in available_methods:
            selected_methods.append("llm_as_judge")

        # Semantic similarity can be a lightweight baseline
        if "semantic_similarity" in available_methods:
            selected_methods.append("semantic_similarity")

        selected_metrics = [
            "correctness",
            "relevance",
            "completeness",
            "groundedness"
        ]

        # Keep faithfulness if it was recommended
        if "faithfulness" in available_metrics:
            selected_metrics.append("faithfulness")

        scoring_mode = "multi_criteria"

        routing_reason.append(
            "The dataset has context/documents, so context-grounded evaluation is appropriate."
        )

        routing_reason.append(
            "Groundedness and completeness should be evaluated because answers should be supported by the provided context."
        )

    # =====================================================
    # Case 4: No-context short/open answer QA
    # =====================================================
    elif has_reference_answer:
        selected_methods = []

        # Use LLM judge for semantic grading
        if "llm_as_judge" in available_methods:
            selected_methods.append("llm_as_judge")
        else:
            selected_methods.append("llm_as_judge")

        # Use exact match as a simple baseline
        if "exact_match_baseline" in available_methods:
            selected_methods.append("exact_match_baseline")

        # Use semantic similarity if available
        if "semantic_similarity" in available_methods:
            selected_methods.append("semantic_similarity")

        selected_metrics = [
            "correctness",
            "relevance",
            "completeness"
        ]

        # If semantic metrics are available, keep them
        if "semantic_similarity" in available_metrics:
            selected_metrics.append("semantic_similarity")

        scoring_mode = "multi_criteria"

        routing_reason.append(
            "The dataset has reference answers but no context, so LLM-as-a-Judge and semantic comparison are suitable."
        )

    # =====================================================
    # Case 5: No reference answer
    # =====================================================
    else:
        selected_methods = ["reference_free_evaluation"]
        selected_metrics = [
            "relevance",
            "clarity"
        ]
        scoring_mode = "reference_free"

        routing_reason.append(
            "The dataset does not contain reference answers, so reference-free evaluation is needed."
        )

    # Remove duplicate methods and metrics while keeping order
    selected_methods = list(dict.fromkeys(selected_methods))
    selected_metrics = list(dict.fromkeys(selected_metrics))

    return {
        "dataset_name": dataset_name,
        "question_type": question_type,
        "has_context": has_context,
        "has_options": has_options,
        "has_reference_answer": has_reference_answer,
        "answer_type": answer_type,
        "reasoning_required": reasoning_required,

        # Main output of this router
        "selected_evaluation_methods": selected_methods,
        "selected_metrics": selected_metrics,
        "scoring_mode": scoring_mode,

        # Human-readable explanation
        "routing_reason": routing_reason
    }
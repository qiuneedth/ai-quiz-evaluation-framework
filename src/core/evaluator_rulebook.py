# ============================================================
# Backward-compatible mapping
# ============================================================
# Your current code may still use old evaluator names:
# - llm_evaluator
# - llm_context_evaluator
#
# To avoid renaming all files now, this mapping converts old names
# into clearer rule names.
# ============================================================

EVALUATOR_RULE_NAME_MAPPING = {
    "llm_evaluator": "semantic_correctness_evaluator",
    "llm_context_evaluator": "context_correctness_evaluator",
}


def resolve_rule_name(evaluator_name: str) -> str:
    """
    Convert an evaluator name into the corresponding rulebook name.

    Example:
        llm_evaluator
        -> semantic_correctness_evaluator

        llm_context_evaluator
        -> context_correctness_evaluator

    If the evaluator name is already a rulebook name, return it directly.
    """

    return EVALUATOR_RULE_NAME_MAPPING.get(evaluator_name, evaluator_name)


def get_evaluator_rule(evaluator_name: str) -> dict:
    """
    Return the rule definition for one evaluator.

    This function is used by EvaluationPlanGenerator.

    It supports both old evaluator names and new clearer rule names.
    """

    rule_name = resolve_rule_name(evaluator_name)

    if rule_name not in EVALUATOR_RULEBOOK:
        raise ValueError(
            f"Unknown evaluator rule: {evaluator_name}. "
            f"Resolved rule name: {rule_name}. "
            f"Available rule names: {list(EVALUATOR_RULEBOOK.keys())}"
        )

    return EVALUATOR_RULEBOOK[rule_name]


def list_available_evaluator_rules() -> list[str]:
    """
    Return all evaluator rule names defined in the rulebook.
    """

    return list(EVALUATOR_RULEBOOK.keys())